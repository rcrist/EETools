# Assigning various data types to variables

# Integers
a = 1
b = 2

# Integer math operators
add = a + b
sub = b - a
mult = a * b
div = a / b

# Print string and values for math operations
print("a + b = ", add, " b - a = ", sub, " a * b = ", mult, " a / b = ", div)  # Print on a single line
print("a + b = ", add, "\nb - a = ", sub, "\na * b = ", mult, "\na / b = ", div)  # Print on multiple lines
